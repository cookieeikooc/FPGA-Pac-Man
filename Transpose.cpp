#include <iostream>
#include <string>
#include <fstream>
using namespace std;

int main()
{
    ifstream fin("input.txt");
    ofstream fout("output.txt");
    if (fin.fail()) {
        cout << "Failed to open input file.";
        exit(1);
    }
    else if (fout.fail()) {
        cout << "Failed to open output file.";
        exit(1);
    }
    
    const int r = 32;
    const int c = 28;
    char arr[r][c];

    for (int i = 0; i < r; i++) {
        for (int j = 0; j < c; j++) {
            fin.get(arr[i][j]);
        }
        fin.ignore();
    }
    
    for (int j = 0; j < c; j++) {
        for (int i = 0; i < r; i++) {
            fout << arr[i][j];
        }
        fout << '\n';
    }
    
    fin.close();
    fout.close();
    return 0;
}

